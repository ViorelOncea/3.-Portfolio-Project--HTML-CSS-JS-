// Image 1
let oneFull = document.getElementById('onefull');
let oneText = document.getElementById('one');
let oneimage = document.getElementById('imageone');

oneFull.addEventListener('mouseenter', function() {
  oneText.style.display = 'none';
  oneimage.style.display = 'inline-block';
});
oneFull.addEventListener('mouseleave', function() {
  oneText.style.display = 'inline-block';
  oneimage.style.display = 'none';
});


